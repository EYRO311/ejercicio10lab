package figure;

/**
 *
 * @author eyro1
 */
public class Rectangle extends Figure {
    private double length, width;

    public Rectangle(String color, double length, double width) {
        super(color);
        this.length = length;
        this.width = width;
    }

    @Override
    public double perimeter() {
        return 2 * (length + width);
    }

    @Override
    public double area() {
        return length * width;
    }

    @Override
    public String getName() {
        return "Rectangle";
    }
}


